    private Usuario recopilarInformacion(){
       Usuario usuario = new Usuario(); 
       usuario.setUsuario(this.jTextFieldUsuario.getText());
       usuario.setNombre(this.jTextFieldNombre.getText());
       usuario.setApellidos(this.jTextFieldApellidos.getText());
       usuario.setIntentos(Integer.parseInt(this.jComboBoxIntentos.getSelectedItem().toString()));
       usuario.setRol(this.jComboBoxRol.getSelectedItem().toString());
       if(String.valueOf(this.jPasswordFieldPass.getPassword()).equals(String.valueOf(this.jPasswordFieldRepass.getPassword()))){
       usuario.setPass(String.valueOf(this.jPasswordFieldPass.getPassword()));  
       }else{
       JOptionPane.showMessageDialog(null, "ERROR!!Las contraseñas no coinciden", "Nuevo Usuario", JOptionPane.ERROR_MESSAGE);
       return null;
       }
       
       return usuario;
    }
