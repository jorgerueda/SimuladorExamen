    @Override
    public void actualizar(int id_evento_vista, Object datos) {
        
                if (id_evento_vista == EventoVista.ALTA_USUARIO_FALLO){
			JOptionPane.showMessageDialog(null, "ERROR!! Ha ocurrido un error", "Nuevo Usuario", JOptionPane.ERROR_MESSAGE);
                        
		}else if (id_evento_vista == EventoVista.ALTA_USUARIO_FALLO_USUARIO_EXISTE){
			JOptionPane.showMessageDialog(null, "ERROR!! El usuario ya existe", "Nuevo Usuario", JOptionPane.ERROR_MESSAGE);
                        VistaUsuario v = new VistaUsuario();
                        v.setVisible(true);
                        v.actualizar(0, datos);
                        
                }else if ( id_evento_vista == 0){
                    
                Usuario usuario = (Usuario)datos; 
                this.jTextFieldUsuario.setText(usuario.getUsuario());
                this.jTextFieldNombre.setText(usuario.getNombre());
                this.jTextFieldApellidos.setText(usuario.getApellidos());
                this.jComboBoxIntentos.setSelectedItem(String.valueOf(usuario.getIntentos()));
                this.jPasswordFieldPass.setText(usuario.getPass());
                this.jPasswordFieldRepass.setText(usuario.getPass());
                this.jComboBoxRol.setSelectedItem(usuario.getRol());
                
                   
                }
    }
