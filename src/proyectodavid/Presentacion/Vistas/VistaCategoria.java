    private Categoria recopilarInformacion(){
        
      Categoria cat = new Categoria();
      Long acierto=(Long)this.jFormattedTextFieldPuntuacionCorrecta.getValue();
      Long minima = (Long)this.jFormattedTextFieldNotaMin.getValue();
      Long time= (Long)this.jFormattedTextFieldTiempoMax.getValue();
      Long preguntas= (Long)this.jFormattedTextFieldNumPreguntas.getValue();
      cat.setNombre(this.jTextFieldName.getText());
      cat.setAcierto(acierto.doubleValue());
      cat.setNotaMinima(minima.intValue());
      cat.setTiempo(time.intValue());
      cat.setNumeroPreguntas(preguntas.intValue());
      cat.setOrden(Integer.parseInt(this.jComboBox1.getSelectedItem().toString()));
      System.out.println(this.jFormattedTextFieldPuntuacionFallo.getValue().getClass().toString());
      if(this.jFormattedTextFieldPuntuacionFallo.getValue().getClass().toString().equalsIgnoreCase("class java.lang.Long")){
        Long fallo=(Long)this.jFormattedTextFieldPuntuacionFallo.getValue();
        cat.setFallo(fallo.doubleValue());
      }else{
          cat.setFallo((Double)this.jFormattedTextFieldPuntuacionFallo.getValue());
      }
      
       
      return cat;  
        
    }
    
    private void iniCombo(){
         int l;
         if(modoEdit){
            l=FactoriaSA.getInstancia().generaSACategoria().mostrarNombresCategoria().size(); 
         }else{
            l=FactoriaSA.getInstancia().generaSACategoria().mostrarNombresCategoria().size()+1; 
         }
        
        Vector<Integer> nc = new Vector<>();
       for (int i = 1; i <= l; ++i) {
       nc.add(i);
        }
    this.jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(nc));
    }
