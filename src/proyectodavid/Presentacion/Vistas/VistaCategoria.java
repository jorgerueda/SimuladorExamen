    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed

       Categoria  categoria = recopilarInformacion();
       if (modoEdit){
      
        categoria.setId(this.c.getId());
        ControladorImp.getInstancia().execute(Evento.MODIFICAR_CATEGORIA, categoria);
       }else{
        ControladorImp.getInstancia().execute(Evento.ALTA_CATEGORIA, categoria);
       }
       this.dispose();
        
        
    }//GEN-LAST:event_jButtonAceptarActionPerformed

